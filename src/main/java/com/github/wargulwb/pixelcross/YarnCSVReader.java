    private static List<String> asXML(final YarnSortiment sortiment) {
        final List<String> lines = new ArrayList<>();
        lines.add("<?xml version=\"1.0\" encoding=\"UTF-8\" standalone=\"yes\" ?>");
        lines.add("<yarndata>");
        asSortimentXML(lines, sortiment);
        lines.add("</yarndata>");
        return lines;
    }

    private static void asSortimentXML(final List<String> lines, final YarnSortiment sortiment) {
        final StringBuilder sb = new StringBuilder()
                .append("  <sortiment producer=\"")
                .append(sortiment.getProducer())
                .append("\"");
        if (sortiment.getName().isPresent()) {
            sb.append(" name=\"").append(sortiment.getName().get()).append("\"");
        }
        sb.append(" >");
        lines.add(sb.toString());
        sortiment.getYarns().forEach(yarn -> asYarnXML(lines, yarn));
        lines.add("  </sortiment>");
    }

    private static void asYarnXML(final List<String> lines, final Yarn yarn) {
        final StringBuilder sb = new StringBuilder()
                .append("    ")
                .append("<yarn id=\"")
                .append(yarn.getId())
                .append("\" name=\"")
                .append(yarn.getName())
                .append("\" rgb=\"")
                .append(new ColorUtils().colorToRGBHexString(yarn.getColor()))
                .append("\" />");
        lines.add(sb.toString());
    }

    private static Yarn convertLine(final String line) {
        final String[] parts = line.split(";");
        return Yarn.newBuilder()
                .withId(parts[0])
                .withName(parts[1])
                .withColor(new ColorUtils().colorFromRGBHexString(parts[2]))
                .build();
    }

